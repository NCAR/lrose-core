#ifndef _PROJ_LATLON_INC_
#define _PROJ_LATLON_INC_

#include <euclid/ProjType.hh>

class ProjLatlon : public ProjType {

public:

   ProjLatlon( Projection *context ) : ProjType( context ) {};
  ~ProjLatlon(){};

   //
   // Required projection methods
   //
   void     updateOrigin(){};

   void     latlon2xy( double lat, double lon, double *x, double *y )
                     { *y = lat; *x = lon; }

   void     xy2latlon( double x, double y, double *lat, double *lon )
                     { *lat = y; *lon = x; }
};

#endif
