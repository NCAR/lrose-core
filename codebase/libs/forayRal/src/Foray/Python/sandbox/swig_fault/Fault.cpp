string Fault::msg(){

    return msg("");
}

//////////////////////////////////////////////////////////////////////
//
//
//
//////////////////////////////////////////////////////////////////////
string Fault::msg(string prefix){
    
    string allMsg;
    vector<string>::iterator ii;

    ii = msg_.begin();
    while(ii != msg_.end()){
	allMsg += (prefix + *ii);
	++ii;
    }

    return allMsg;

}

