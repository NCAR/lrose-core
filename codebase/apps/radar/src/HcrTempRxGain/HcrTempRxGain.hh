#define HcrTempRxGain_HH

#include "Args.hh"
#include "Params.hh"
#include "TimeSample.hh"
#include <string>
#include <vector>
#include <toolsa/TaXml.hh>
#include <toolsa/DateTime.hh>
using namespace std;

class HcrTempRxGain {
  
public:

  // constructor
  
  HcrTempRxGain (int argc, char **argv);

  // destructor
  
  ~HcrTempRxGain();

  // run 

  int Run();

  // data members

  int OK;

protected:
private:

  string _progName;
  char *_paramsPath;
  Args _args;
  Params _params;

  // archive mode
  
  time_t _archiveStartTime, _archiveEndTime;

  // data retrieval

  time_t _retrieveStartTime, _retrieveEndTime;
  vector<TimeSample> _samples;

  // methods

  int _runArchive();
  int _runRealtime();
  int _processRealtime(time_t now);
  int _processArchive(time_t archiveTime);
  int _retrieveFromSpdb();
  int _processTime(time_t procTime);
  int _writeToSpdb(time_t procTime,
                   const TimeSample &procSample);

};

#endif
