///////////////////////////////////////////////////////////////////////////////
// Keeps latitude and longitude in deg, min, and sec
///////////////////////////////////////////////////////////////////////////////

#ifndef _EDGE_LAT_LON_
#define _EDGE_LAT_LON_

class LatLon {

public:

   LatLon( int deg, int min, int sec );
   LatLon( double val );
   ~LatLon(){};

   void   set( double val );
   void   set( int deg, int min, int sec );

   double getValue(){ return( value ); }
   int    getDegrees(){ return( degrees ); }
   int    getMinutes(){ return( minutes ); }
   int    getSeconds(){ return( seconds ); }

private:

   int degrees;
   int minutes;
   int seconds;
   
   double value;
   
};

#endif

   
