#define DropEOVStrategy_hh


#include "EOVStrategy.hh"
using namespace std;


class DropEOVStrategy : public EOVStrategy
{

public:

  DropEOVStrategy(const double elevation_drop);
  virtual ~DropEOVStrategy();
   
  virtual bool isEndOfVolume(const double elevation);

private:

  double _elevationDrop;
  
  double _maxElevInVol;
  
};

#endif
