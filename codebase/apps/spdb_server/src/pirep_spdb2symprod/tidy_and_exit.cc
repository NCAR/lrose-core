#include "pirep_spdb2symprod.hh"

/*********************************************************************
 * tidy_and_exit() - Clean up any memory, etc. and exit the program.
 */

void tidy_and_exit(int sig)
{

  // verify mallocs

  umalloc_map();
  umalloc_verify();

  // Unregister from the process mapper

  PMU_auto_unregister();
  
  // Delete the server object

  if (Spdb_server != NULL)
    delete Spdb_server;
  
  // exit with code sig

  exit(sig);

}
