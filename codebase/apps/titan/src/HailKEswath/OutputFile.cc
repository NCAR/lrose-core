// Constructor

OutputFile::OutputFile(const string &prog_name, const Params &params) :
  _progName(prog_name), _params(params)
  
{
   _nFields = 2;
   _maxHailMassFluxFieldNum = 0;
   _maxHailKeFluxFieldNum = 1 ; 

}

