/*******************************************************************************
 * increment_count.c
 *
 * increments counts in one struct based on another
 *
 * Mike Dixon  RAP NCAR Boulder CO USA
 *
 * January 1992
 *
 *******************************************************************************/

#include "verify_tracks.h"

void increment_count(vt_count_t *general,
		     vt_count_t *specific)

{

  general->n_success += specific->n_success;
  general->n_failure += specific->n_failure;
  general->n_false_alarm += specific->n_false_alarm;
  general->n_non_event += specific->n_non_event;

}
