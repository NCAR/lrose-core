/*******************************************************************************
 * set_counts_to_longs.c
 *
 * sets the counts to longs, for file storage
 *
 * Mike Dixon  RAP NCAR Boulder CO USA
 *
 * January 1992
 *
 *******************************************************************************/

#include "verify_tracks.h"

void set_counts_to_longs(vt_count_t *double_count,
			 track_file_contingency_data_t *long_count)

{

  long_count->n_success =
    (long) (double_count->n_success + 0.5);
  
  long_count->n_failure =
    (long) (double_count->n_failure + 0.5);
    
  long_count->n_false_alarm =
    (long) (double_count->n_false_alarm + 0.5);

}
