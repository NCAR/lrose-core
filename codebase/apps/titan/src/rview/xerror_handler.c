#include "rview.h"

#define MSG_LENGTH 80

int xerror_handler(Display *display,
		   XErrorEvent *err)

{

  char msg[MSG_LENGTH];

  XGetErrorText(display, err->error_code, msg, MSG_LENGTH);
  fprintf(stderr, "X error code %s\n", msg);
  fprintf(stderr, "Resource ID %ld\n", err->resourceid);

  return (0);

}

